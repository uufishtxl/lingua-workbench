from django.db import models
from django.conf import settings
from django.utils import timezone


class Scenario(models.Model):
    """
    场景定义：存储 Prompts, Roles, Icons。
    system_prompt 可由后端根据 title + description 自动合成。
    """
    user = models.ForeignKey(
        settings.AUTH_USER_MODEL, on_delete=models.CASCADE,
        null=True, blank=True, help_text="Null for global/preset scenarios"
    )
    title = models.CharField(max_length=200)
    description = models.TextField(blank=True)
    icon = models.CharField(max_length=50, default="☕")
    system_prompt = models.TextField(
        blank=True, default='',
        help_text="Auto-generated from title + description if left blank"
    )

    is_preset = models.BooleanField(default=False)
    created_at = models.DateTimeField(auto_now_add=True)

    def __str__(self):
        return self.title


class Conversation(models.Model):
    """
    会话：归属用户 + 场景，存储动态 Summary。
    """
    user = models.ForeignKey(settings.AUTH_USER_MODEL, on_delete=models.CASCADE)
    scenario = models.ForeignKey(Scenario, on_delete=models.CASCADE)
    summary = models.TextField(blank=True, help_text="Rolling summary generated by LLM")
    is_active = models.BooleanField(default=True)
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    def __str__(self):
        return f"{self.user} - {self.scenario.title} ({self.created_at.date()})"


class PracticeMessage(models.Model):
    """
    核心流水表 — 存储 role, user_content, tutor feedback, character reply, status。
    契约字段映射：
      - user_content       → 用户原始输入
      - tutor_polished_text → Tutor 润色后的英文
      - tutor_explanation_cn → Tutor 中文解析
      - character_content   → Character 角色的回复文本
      - audio_url           → TTS 生成的音频 URL
      - status / is_processed → 异步处理状态跟踪
    """
    class Role(models.TextChoices):
        USER = 'user', 'User'
        ASSISTANT = 'assistant', 'Assistant'

    class Status(models.TextChoices):
        PENDING = 'PENDING', 'Pending'
        PROCESSING = 'PROCESSING', 'Processing'
        SUCCESS = 'SUCCESS', 'Success'
        FAILED = 'FAILED', 'Failed'

    conversation = models.ForeignKey(
        Conversation, on_delete=models.CASCADE, related_name='messages'
    )
    role = models.CharField(max_length=20, choices=Role.choices)
    status = models.CharField(
        max_length=20, choices=Status.choices, default=Status.PENDING
    )
    is_processed = models.BooleanField(default=False)

    # --- User message fields ---
    user_content = models.TextField(blank=True, default='')

    # --- Tutor feedback (populated async, only for user messages) ---
    tutor_polished_text = models.TextField(blank=True, default='')
    tutor_explanation_cn = models.TextField(blank=True, default='')

    # --- Character reply (populated async, only for assistant messages) ---
    character_content = models.TextField(blank=True, default='')
    audio_url = models.CharField(
        max_length=500, blank=True, default='',
        help_text="Relative URL to generated TTS WAV file"
    )

    created_at = models.DateTimeField(auto_now_add=True)

    class Meta:
        ordering = ['created_at']

    def __str__(self):
        preview = (self.user_content or self.character_content)[:40]
        return f"[{self.role}] {preview}..."


class PracticeFlashcard(models.Model):
    """
    划词制卡：存储 targetPhrase, promptQuestion, answer, exampleContext
    以及 SRS 指标 (boxLevel, nextReviewAt)。
    """
    user = models.ForeignKey(settings.AUTH_USER_MODEL, on_delete=models.CASCADE)
    message = models.ForeignKey(
        PracticeMessage, on_delete=models.SET_NULL,
        null=True, blank=True, related_name='flashcards',
        help_text="The message this flashcard was extracted from"
    )

    target_phrase = models.CharField(max_length=300)
    prompt_question = models.TextField(help_text="The 'Q' prompt for review")
    answer = models.TextField(help_text="Expected answer")
    example_context = models.TextField(blank=True, default='')

    # SRS fields
    box_level = models.PositiveIntegerField(default=1, help_text="Leitner box 1-5")
    next_review_at = models.DateTimeField(default=timezone.now)

    created_at = models.DateTimeField(auto_now_add=True)

    def __str__(self):
        return self.target_phrase


class WordNode(models.Model):
    """
    知识图谱节点：存储词汇/短语及其语义向量。
    explanation + example 由 Huey task 异步填充。
    """
    class NodeType(models.TextChoices):
        KEYWORD = 'keyword', 'Keyword'
        PHRASE = 'phrase', 'Phrase'

    class Status(models.TextChoices):
        PENDING = 'PENDING', 'Pending'
        SUCCESS = 'SUCCESS', 'Success'
        FAILED = 'FAILED', 'Failed'

    user = models.ForeignKey(settings.AUTH_USER_MODEL, on_delete=models.CASCADE)
    phrase_log = models.OneToOneField(
        'phrase_log.PhraseLog', on_delete=models.CASCADE,
        null=True, blank=True, related_name='vocab_node'
    )

    label = models.CharField(max_length=200)
    node_type = models.CharField(
        max_length=20, choices=NodeType.choices, default=NodeType.KEYWORD
    )
    explanation = models.TextField(blank=True, default='')
    example = models.TextField(blank=True, default='')
    status = models.CharField(
        max_length=20, choices=Status.choices, default=Status.PENDING,
        help_text="LLM enrichment status"
    )

    # SRS fields (synced with phrase_log or managed here)
    mastery = models.PositiveIntegerField(default=0, help_text="0-100 percentage")
    box_level = models.PositiveIntegerField(default=1, help_text="SRS box level (1-5)")
    next_review_at = models.DateTimeField(
        default=timezone.now,
        help_text="SRS next review timestamp"
    )

    # LLM-generated scenarios for Daily Phrases
    scenarios = models.JSONField(
        default=list, blank=True,
        help_text="LLM-generated scenarios: [{'description': '...', 'tag': 'Office'}, ...]"
    )

    embedding = models.JSONField(
        null=True, blank=True,
        help_text="1536-dim vector for semantic search/linking"
    )

    created_at = models.DateTimeField(auto_now_add=True)

    def __str__(self):
        return self.label


class WordLink(models.Model):
    """
    知识图谱连线：存储词汇间的关联关系。
    """
    user = models.ForeignKey(settings.AUTH_USER_MODEL, on_delete=models.CASCADE)

    source_type = models.CharField(max_length=20, help_text="e.g., 'scenario' or 'vocab'")
    source_id = models.CharField(max_length=50)
    target_type = models.CharField(max_length=20, help_text="e.g., 'vocab'")
    target_id = models.CharField(max_length=50)

    relation = models.CharField(
        max_length=50, default="context",
        help_text="e.g., 'context', 'synonym', 'contrast'"
    )

    created_at = models.DateTimeField(auto_now_add=True)

    class Meta:
        unique_together = (
            'user', 'source_type', 'source_id',
            'target_type', 'target_id', 'relation'
        )


class DailyPracticeLog(models.Model):
    """
    每日练习日志：追踪用户当天的 3-word session 进度和完成状态。
    focus_minutes is derived from (updated_at - created_at), not stored.
    """
    user = models.ForeignKey(settings.AUTH_USER_MODEL, on_delete=models.CASCADE)
    date = models.DateField(default=timezone.now)
    words_practiced = models.IntegerField(default=0)
    word_ids = models.JSONField(
        default=list,
        help_text="3 WordNode IDs for today: [12, 45, 89]"
    )
    is_completed = models.BooleanField(default=False)
    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    class Meta:
        constraints = [
            models.UniqueConstraint(
                fields=['user', 'date'],
                name='unique_daily_log'
            )
        ]

    def __str__(self):
        return f"{self.user} - {self.date} ({self.words_practiced}/3)"

    @property
    def focus_minutes(self):
        """Rough calculation: minutes between session start and last update."""
        if self.updated_at and self.created_at:
            delta = self.updated_at - self.created_at
            return round(delta.total_seconds() / 60)
        return 0
